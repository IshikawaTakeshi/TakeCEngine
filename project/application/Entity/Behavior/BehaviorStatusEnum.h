//==================================================================================

enum class BehaviorStatus {

	Invalid = -1, // 無効な状態
	Success = 0,  // 成功(ノードを実行完了した)
	Failure = 1,  // 失敗(ノードを実行出来なかった)
	Running = 2,  // 実行中(ノードを実行出来たが、まだ完了していない)
	Aborted = 3   // 中止(ノードの実行が中止された)
};